		METODE PEMBELAJARAN DAN UJIAN

		Metode Pembelajaran adalah sebagai berikut:
		1. Kuliah pengayaan
		2. Clinical science session/Referat/Journal Reading
		3. Case report session/Tutorial
		4. Resource person session
		5. Bed site teaching


		Metode Ujian adalah sebagai berikut
		1. Mini Clinical Examination (Mini C-Ex)
		2. Ujian Long case
		3. Ujian Tulis (Pre test dan Post test)
		4. Ujian Structure Oral Case Analysis (SOCA)
		5. Ujian Objective Structure Cliical Examination (OSCE)


