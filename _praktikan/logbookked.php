            <li>Mahasiswa tahap profesi wajib melakukan anamnesis, pemeriksaan fisik, menentukan diagnosis dan merencanakan tindakan tatalaksana secara mandiri dengan jumlah minimal kasus ditentukan oleh kordinator pendidikan selama kegiatan di poliklinik di bawah supervisi pembimbing.</li>
            <li>&nbsp;</li>
