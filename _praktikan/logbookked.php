        <strong>Tugas dan wewenang di Poliklinik Jiwa : </strong>
        <ol>
            <li>Mahasiswa tahap profesi bekerja sesuai jadwal yang telah dibuat di poliklinik Jiwa dan aktif melakukan anamnesis, pemeriksaan fisik, mengobservasi tatalaksana pasien di poliklinik di bawah bimbingan dokter yang bertugas di poliklinik.</li>
            <li>Mahasiswa tahap profesi wajib melakukan anamnesis, pemeirksaan fisik, menentukan diagnosis dan merencanakan tindakan tatalaksana secara mandiri dengan jumlah minimal kasus ditentukan oleh kordinator pendidikan selama kegiatan di poliklinik di bawah supervisi pembimbing.</li>
            <li>Pembimbing melakukan supervisi anamnesis dan pemeriksaan psikiatri, menentukan diagnosis dan merencanakan tindakan tatalaksana terhadap pasien setiap mahasiswa tahap profesi dan bila mahasiswa tahap profesi telah dinilai mampu maka pembimbing menandatangani buku log.</li>
        </ol>
